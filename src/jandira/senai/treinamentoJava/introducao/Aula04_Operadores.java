package jandira.senai.treinamentoJava.introducao;

public class Aula04_Operadores {
    public static void main (String [] args){
        float numero01 = 10;
        float numero02 = 20;
        System.out.println(numero01 + numero02);
        System.out.println(numero01 - numero02);
        System.out.println(numero01 / numero02);
        System.out.println(numero01 * numero02);
    }
}
