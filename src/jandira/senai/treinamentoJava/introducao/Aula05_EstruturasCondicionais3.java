package jandira.senai.treinamentoJava.introducao;

public class Aula05_EstruturasCondicionais3 {
    public static void main (String [] args){

        double salario = 3000;

        //(condição) ? verdadeira : falsa
        String resultado = salario > 5000 ? "Eu vou doar 500 reais" : "Eu não tenho dinheiro para doar";
        System.out.println(resultado);
    }
}
